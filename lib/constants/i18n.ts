export const translations: Record<AppLanguage, TranslationDictionary> = { vi, en };

export function translate(
  language: AppLanguage,
  key: string,
  params?: Record<string, string | number>
) {
  const template =
    translations[language]?.[key as TranslationKey] ??
    translations.vi[key as TranslationKey] ??
    key;
  if (!params) {
    return template;
  }

  return Object.entries(params).reduce((value, [paramKey, paramValue]) => {
    return value.replaceAll(`{${paramKey}}`, String(paramValue));
  }, template);
}
